export const useConfetti = (show: boolean) => {
    useEffect(() => {
        if (!show) return;

        const end = Date.now() + 2000;
        let frameId: number;

        const bursts = [
            { angle: 60, origin: { x: 0 } },
            { angle: 120, origin: { x: 1 } },
        ];

        const animate = () => {
            bursts.forEach(({ angle, origin }) => {
                confetti({
                    particleCount: 3,
                    angle,
                    spread: 60,
                    origin,
                    shapes: SHAPES,
                    colors: COLORS,
                });
            });

            if (Date.now() < end) {
                frameId = requestAnimationFrame(animate);
            }
        };

        animate();

        return () => cancelAnimationFrame(frameId);
    }, [show]);
};
